#Площадь треугольника формула герона
#напишите программу, вычисляющую площадь треугольника по переданным длинам трёх его сторон по формуле Герона:
#S=(p(p-a)(p-b)(p−c))^0.5, где p=(a+b+c)/2 – полупериметр треугольника. 
# На вход программе подаются целые числа, выводом программы должно являться вещественное число,
#соответствующее площади треугольника.
a = int(input())
b = int(input())
c = int(input())
p = (a + b + c) /2
S = (p*(p-a)*(p-b)*(p-c)) ** 0.5
print(S)